package com.vo;

public class GoldenContinentAreaData{
    private int id;

    private int area;

    private int type;

    private int diamonds;

    private int boxId;

    private int remain;

    public void setId(int id) {
        this.id =id;
    }

    public int getId(){
        return this.id;
    }

    public void setArea(int area) {
        this.area =area;
    }

    public int getArea(){
        return this.area;
    }

    public void setType(int type) {
        this.type =type;
    }

    public int getType(){
        return this.type;
    }

    public void setDiamonds(int diamonds) {
        this.diamonds =diamonds;
    }

    public int getDiamonds(){
        return this.diamonds;
    }

    public void setBoxId(int boxId) {
        this.boxId =boxId;
    }

    public int getBoxId(){
        return this.boxId;
    }

    public void setRemain(int remain) {
        this.remain =remain;
    }

    public int getRemain(){
        return this.remain;
    }

}